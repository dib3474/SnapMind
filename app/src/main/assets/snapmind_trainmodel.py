# %% [12] 안드로이드 배포용 TFLite 변환 ----------------------
import tensorflow as tf

model = tf.keras.models.load_model("image_classifier.keras")

converter = tf.lite.TFLiteConverter.from_keras_model(model)
# 안드로이드 권장: 동적 범위 양자화 (용량 약 1/4, 속도↑, 정확도 손실 거의 없음)
converter.optimizations = [tf.lite.Optimize.DEFAULT]
tflite_model = converter.convert()

with open("image_classifier.tflite", "wb") as f:
    f.write(tflite_model)

# 라벨 순서 저장 (앱에서 출력 인덱스 → 클래스 이름 매핑에 필수)
with open("labels.txt", "w", encoding="utf-8") as f:
    f.write("\n".join(class_names))

print("변환 완료:", class_names)

